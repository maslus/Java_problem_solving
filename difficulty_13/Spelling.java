package difficulty_13;/*
У студента-филолога Васи есть замечательный друг Петя. И Петя никак не может выучить английский язык.
Английский текст Петя еще кое-как читает, но пишет с ужасными ошибками, причем чаще всего он вставляет
в слова лишние буквы.

Вася решил помочь Пете. Теперь каждый день Вася диктует Пете слова, а Петя их записывает.
После семестра занятий Петя стал писать много лучше и теперь делает в словах только по одной ошибке.
Чтобы автоматизировать процесс исправления ошибок, Вася просит Вас написать программу, которая удаляет из слова
одну лишнюю букву и показывает Пете правильное слово.

Входные данные
Входной файл INPUT.TXT содержит целое число K - номер лишней буквы, а затем через один или несколько пробелов
записано слово S, состоящее из английских букв верхнего регистра. Гарантируется, что номер буквы не превышает длину
слова. Длина слова не более 80 символов.

Выходные данные
В выходной файл OUTPUT.TXT выведите исправленное слово.

Примеры
№	INPUT.TXT	OUTPUT.TXT
1	4 MISTSPELL	MISSPELL
2	2       ABC	AC
 */


import java.io.*;
import java.util.*;

public class Spelling {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        PrintWriter out = new PrintWriter(System.out);

        byte K = in.nextByte();
        String S = in.nextLine();

        String tmp = S.replace(" ", "");
        S = tmp;

        StringBuffer sb = new StringBuffer(S);
        sb.delete(K - 1, K);

        out.println(sb);
        out.flush();
    }
}