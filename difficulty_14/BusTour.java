package difficulty_14;/*
Оргкомитет Московской городской олимпиады решил организовать обзорную экскурсию по Москве для участников олимпиады.
Для этого был заказан двухэтажный автобус (участников олимпиады достаточно много и в обычный они не умещаются)
высотой 437 сантиметров. На экскурсионном маршруте встречаются N мостов. Жюри и оргкомитет олимпиады очень обеспокоены
тем, что высокий двухэтажный автобус может не проехать под одним из них. Им удалось выяснить точную высоту каждого из
мостов. Автобус может проехать под мостом тогда и только тогда, когда высота моста превосходит высоту автобуса.

Помогите организаторам узнать, закончится ли экскурсия благополучно, а если нет, то установить, где произойдет авария.

Входные данные
Во входном файле INPUT.TXT сначала содержится число N (1 ≤ N ≤ 1000). Далее идут N натуральных чисел, не превосходящих
 10000 - высоты мостов в сантиметрах в том порядке, в котором они встречаются на пути автобуса.

Выходные данные
В единственную строку выходного файла OUTPUT.TXT нужно вывести фразу "No crash", если экскурсия закончится благополучно.
 Если же произойдет авария, то нужно вывести сообщение "Crash k", где k - номер моста, где произойдет авария. Фразы
 выводить без кавычек ровно с одним пробелом внутри.

Примеры
№	INPUT.TXT	OUTPUT.TXT
1	1
763	No crash
2	3
763 245 113	Crash 2
3	1
437	Crash 1
 */

import java.io.*;
import java.util.*;

public class BusTour{
    public static void main(String[] args){
        Scanner in = new Scanner(System.in);
        PrintWriter out = new PrintWriter(System.out);

        short N = in.nextShort();
        short[]bridges = new short[N];
        for (int i = 0; i < N; i++) {
            bridges[i]  = in.nextShort();
            if (bridges[i]<=437){
                out.println("Crash "+(i+1));
                out.flush();
                break;
            } if (bridges[N-1]>437){
                out.println("No crash");
                out.flush();
            }
        }
    }
}
